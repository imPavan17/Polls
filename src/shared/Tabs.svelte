<script>
    import { createEventDispatcher } from 'svelte';

    export let items;
    export let activeTab;

    const dispatcher = createEventDispatcher();

    const toggleTab = (item) => {
        dispatcher('toggleTab', item)
    }
</script>

<div class="tabs">
    <ul>
        {#each items as item}
            <li on:click={() => toggleTab(item)}>
                <div class:active={item === activeTab}>{item}</div>
            </li>
        {/each}
    </ul>
</div>

<style>
    .tabs {
        margin-bottom: 40px;
    }

    ul {
        display: flex;
        justify-content: center;
        padding: 0;
        list-style: none;
    }

    li {
        margin: 0 16px;
        font-size: 18px;
        cursor: pointer;
        color: #555;
    }

    .active {
        color: #d91b42;
        border-bottom: 2px solid #d91b42;
        padding-bottom: 8px;
    }
</style>